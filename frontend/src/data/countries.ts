export const getCountryCode = (country: string): string => {
  if (!country) return '';
  const normalized = country.trim();
  // Try exact match first
  let found = COUNTRIES.find(c => c.value === normalized);
  // If no exact match, try case-insensitive match
  if (!found) {
    found = COUNTRIES.find(c => c.value.toLowerCase() === normalized.toLowerCase());
  }
  // If still no match, try matching the first part before comma (e.g., "Hong Kong, China" -> "Hong Kong")
  if (!found && normalized.includes(',')) {
    const firstPart = normalized.split(',')[0].trim();
    found = COUNTRIES.find(c => c.value.toLowerCase() === firstPart.toLowerCase());
  }
  return found?.code || '';
};

